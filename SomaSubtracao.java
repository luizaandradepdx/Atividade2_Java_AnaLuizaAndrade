public class SomaSubtracao {
    public static void main(String[] args) {
        int numero1 = 15;
        int numero2 = 7;

        int soma = numero1 + numero2;
        int subtracao = numero1 - numero2;

        System.out.println("Soma: " + soma);
        System.out.println("Subtração: " + subtracao);
    }
}
